#Operators->Which is used to perform operations on values and variables
#Arthimetic Operators
a=4
b=2
print("Sum:",a+b)#Addition
print("Substraction:",a-b)#Substraction
print("Multiply:",a*b)#Multiply
print("divison:",a/b)#Divison
print("Floor:",a//b)#Floor
print("modulo:",a%b)#Modulo
print("Power:",a**b)#Power

#Assignment Operators
a=1
